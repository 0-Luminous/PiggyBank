import SwiftUI

extension Font {
    static func nunitoSans(_ size: CGFloat) -> Font {
        .custom("NunitoSans-Regular", size: size)
    }
    
    static func nunitoSansBold(_ size: CGFloat) -> Font {
        .custom("NunitoSans-Bold", size: size)
    }
    
    static func nunitoSansSemiBold(_ size: CGFloat) -> Font {
        .custom("NunitoSans-SemiBold", size: size)
    }
    
    static func nunitoSansExtraBold(_ size: CGFloat) -> Font {
        .custom("NunitoSans-ExtraBold", size: size)
    }
}

